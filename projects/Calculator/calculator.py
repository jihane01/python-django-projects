num1 = int(input('Enter first number : '))
num2 = int(input('Enter Second number : '))

op = input('Enter the Operator  ')

if op ==  '+' :
    print(num1+num2)
elif op == '-':
        print(num1-num2)
elif op == '*':
      print(num1 *num2)
elif op == '/':
      print(num1/num2)
else:
      print('Inavalid Operator Enter + or - or * or /')        


